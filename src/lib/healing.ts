// 获取每日语录（基于日期的确定性选择）
export function getDailyQuote(): HealingQuote {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000
  );
  return HEALING_QUOTES[dayOfYear % HEALING_QUOTES.length];
}

// 获取随机语录
export function getRandomQuote(category?: HealingQuote['category']): HealingQuote {
  const pool = category
    ? HEALING_QUOTES.filter(q => q.category === category)
    : HEALING_QUOTES;
  return pool[Math.floor(Math.random() * pool.length)];
}

// 按分类获取语录
export function getQuotesByCategory(category: HealingQuote['category']): HealingQuote[] {
  return HEALING_QUOTES.filter(q => q.category === category);
}

