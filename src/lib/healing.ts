// 根据心情标签获取智能回复
export function getMoodReply(moodTag: string, moodScore: number): string {
  const moodData = MOOD_RESPONSES.find(m => m.moodTag === moodTag);
  if (!moodData) {
    return '谢谢你记录今天的心情。每一天的记录都是对自己的关爱。';
  }

  let prefix = '';
  if (moodScore <= 3) {
    prefix = '今天似乎很不容易。';
  } else if (moodScore <= 5) {
    prefix = '';
  } else if (moodScore >= 8) {
    prefix = '看起来今天心情不错！';
  }

  const responses = moodData.responses;
  const idx = Math.floor(Math.random() * responses.length);
  return prefix ? `${prefix}${responses[idx]}` : responses[idx];
}

